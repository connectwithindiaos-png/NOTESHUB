export function getSubjects(courseSlug, semesterId) {
  const course = subjectsData[courseSlug];
  if (!course) return [];
  return course[semesterId] || [];
}

export function getSubjectBySlug(courseSlug, semesterId, subjectSlug) {
  const subjects = getSubjects(courseSlug, semesterId);
  return subjects.find(s => s.slug === subjectSlug) || null;
}

export function getSubjectById(courseSlug, semesterId, subjectId) {
  const subjects = getSubjects(courseSlug, semesterId);
  return subjects.find(s => s.id === subjectId) || null;
}

export function getAllSubjectSlugs() {
  const slugs = [];
  for (const [course, semesters] of Object.entries(subjectsData)) {
    for (const [semId, subs] of Object.entries(semesters)) {
      for (const sub of subs) {
        slugs.push({ course, semester: `semester-${semId}`, subject: sub.slug });
      }
    }
  }
  return slugs;
}
