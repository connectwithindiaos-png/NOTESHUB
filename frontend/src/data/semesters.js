export function getSemesters(courseSlug) {
  return semesterData[courseSlug] || [];
}

export function getSemester(courseSlug, semesterSlug) {
  const sems = semesterData[courseSlug] || [];
  return sems.find(s => s.slug === semesterSlug) || null;
}

export function getAllSemesterSlugs() {
  const slugs = [];
  for (const [course, sems] of Object.entries(semesterData)) {
    for (const sem of sems) {
      slugs.push({ course, semester: sem.slug });
    }
  }
  return slugs;
}
